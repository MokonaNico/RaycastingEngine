double movementSpeed = 5.0;
double rotationSpeed = 3.0;

Vector pos = {22,12};
Vector dir = {-1,0};
Vector plane = {0,0.66};

int get_world_case(int x, int y){
    return worldMap[x][y];
}

void doMovements(double t){
    double move = t * movementSpeed; //the constant value is in squares/second
    double rot = t * rotationSpeed; //the constant value is in radians/second
    if(up_pressed){
        if(get_world_case((int)(pos.x + dir.x * move), (int)pos.y) == 0) pos.x += dir.x * move;
        if(get_world_case((int)pos.x, (int)(pos.y+dir.y*move)) == 0) pos.y += dir.y * move;
    }
    if(down_pressed){
        if(get_world_case((int)(pos.x - dir.x * move), (int)pos.y) == 0) pos.x -= dir.x * move;
        if(get_world_case((int)pos.x, (int)(pos.y-dir.y*move)) == 0) pos.y -= dir.y * move;
    }
    if(left_pressed){
        dir = rotate(dir, rot);
        plane = rotate(plane, rot);
    }
    if(right_pressed){
        dir = rotate(dir, -rot);
        plane = rotate(plane, -rot);
    }
}

